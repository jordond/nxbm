import * as compare from "compare-semver";

export function compareGameRevision(oldVer: string, newVer: string): string {
  if (oldVer && !newVer) {
    return oldVer;
  }

  if (newVer && !oldVer) {
    return newVer;
  }

  if (!oldVer && !newVer) {
    return "";
  }

  try {
    return compare.max([oldVer, newVer]);
  } catch (error) {
    return "";
  }
}

export function findVersion(files: string[]): string {
  return findVersionByFiles(files) || findVersionByCount(files) || "unknown";
}

/**
 * Find the version number by comparing the number of update files,
 * and if they match the known update files.
 * @param files List of update files in XCI
 */
export function findVersionByFiles(files: string[]) {
  return Object.keys(UPDATE_FILES).find(
    key =>
      !!findVersionByCount(files) &&
      files.every(file => (UPDATE_FILES[key] as string[]).includes(file))
  );
}

/**
 * A less acurate way to determine the file version.
 * Only checks the number of update files
 * @param files List of update files in XCI
 */
export function findVersionByCount(files: string[]) {
  return Object.keys(UPDATE_NUMBER_OF_FILES).find(
    key => files.length === UPDATE_NUMBER_OF_FILES[key]
  );
}

